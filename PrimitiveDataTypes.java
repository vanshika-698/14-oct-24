public class PrimitiveDataTypes {
    public static void main(String[] args) {
        // Integer
        int age = 25;
        System.out.println("Integer: " + age);

        // Float
        float price = 19.99f;
        System.out.println("Float: " + price);

        // Double (more precision than float)
        double largePrice = 199.99;
        System.out.println("Double: " + largePrice);

        // Character
        char grade = 'A';
        System.out.println("Character: " + grade);

        // String
        String name = "Vanshika";
        System.out.println("String: " + name);

        // Boolean
        boolean isStudent = true;
        System.out.println("Boolean: " + isStudent);

        // Long (larger integer values)
        long population = 7800000000L;
        System.out.println("Long: " + population);

        // Byte (small integer value)
        byte smallNumber = 100;
        System.out.println("Byte: " + smallNumber);

        // Short (integer value with less range than int)
        short smallRangeNumber = 32000;
        System.out.println("Short: " + smallRangeNumber);
    }
}
